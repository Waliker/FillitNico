unsigned int	ft_atobin(char *str)
{
	int				j;
	int				n;
	unsigned int	a;

	j = 0;
	a = 0;
	n = 0;
	while (str[j] != 0)
	{
		if (str[j] == '\n')
			n++;
		if (str[j] == '#')
			a = a | (1 << (j - n));
		j++;
	}
	return (a);
}
